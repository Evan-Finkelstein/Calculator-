







export function addTwoNumbers(number1, number2) {

    return number1 + number2

}

export function subTwoNumbers(number1, number2) {
    
    return number1 - number2

}

export function multTwoNumbers(number1, number2) {
    
    return number1 * number2

}

export function divTwoNumbers(number1, number2) {
    
    return number1 / number2

}